# ---------------------------------------------------------------------------

@mcp.tool(
    name="zotero_graph_explore_item",
    description="Explore the full graph context of a Zotero item — its community, tags, and all neighbors sorted by connection strength."
)
def graph_explore_item(
    item_key: str,
    neighbor_limit: int = 10,
    *,
    ctx: Context
) -> str:
    """Get the complete graph context for a single Zotero item.

    Returns: title, community, tags, total connections, and neighbors
    sorted by edge weight (strongest connections first).

    Args:
        item_key: Zotero item key (8-char alphanumeric, e.g. "ABCD1234").
        neighbor_limit: Max neighbors to list (default 10).
    """
    try:
        err = _ensure_graph()
        if err:
            return err

        data = _load_graph_data()
        nodes_map = _build_nodes_map(data)
        edge_index = _build_edge_index(data)

        node = nodes_map.get(item_key)
        if not node:
            return f"Item key '{item_key}' not found in graph."

        cid = node.get("community")
        comm_label = node.get("community_label", "Unknown")
        tags = ", ".join(node.get("tags_normalized", [])[:8])
        degree = len(edge_index.get(item_key, []))

        output: list[str] = []
        output.append(f"# {node.get('label', item_key)}")
        output.append(f"**Community:** {comm_label} (#{cid})")
        output.append(f"**Tags:** {tags}")
        output.append(f"**Connections:** {degree}")
        output.append("")

        neighbors = _format_neighbors(item_key, edge_index, nodes_map, limit=neighbor_limit)
        if neighbors:
            output.append("## Neighbors (by connection strength)")
            output.extend(neighbors)

        return "\n".join(output)

    except Exception as e:
        ctx.error(f"Error exploring item: {str(e)}")
        return f"Error exploring item: {str(e)}"


