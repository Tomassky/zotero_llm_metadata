@mcp.tool(
    name="zotero_graph_community_info",
    description="Browse community structure in your Zotero knowledge graph. No argument returns all communities; provide a label to get community detail with member list."
)
def graph_community_info(
    community_label: str = "",
    top_n: int = 15,
    *,
    ctx: Context
) -> str:
    """List all communities or get details for a specific community.

    No argument → returns overview of all communities with sizes and cohesion.
    With community_label → returns members list, cohesion, and graph stats.

    Args:
        community_label: Community name to inspect (e.g. "红队技术", "大语言模型").
            Empty string returns all communities.
        top_n: Max members to list for a specific community (default 15).
    """
    try:
        err = _ensure_graph()
        if err:
            return err

        data = _load_graph_data()
        nodes_map = _build_nodes_map(data)
        edge_index = _build_edge_index(data)

        output: list[str] = []

        # No label → overview
        if not community_label:
            output.append("# Knowledge Graph Communities")
            for cid_str, comm in data.get("communities", {}).items():
                label = comm.get("label", f"Community {cid_str}")
                size = comm.get("size", len(comm.get("members", [])))
                cohesion = comm.get("cohesion", 0)
                output.append(f"- **{label}** ({size} items, cohesion: {cohesion})")
            stats = data.get("stats", {})
            validation = data.get("validation", {})
            output.append("")
            output.append(
                f"Total: {stats.get('nodes', 0)} items · {stats.get('edges', 0)} edges "
                f"· {stats.get('avg_degree', 0)} avg degree · Modularity: {validation.get('modularity', 0)}"
            )
            return "\n".join(output)

        # Find matching community
        label_lower = community_label.lower()
        matched = None
        for cid_str, comm in data.get("communities", {}).items():
            clabel = comm.get("label", "").lower()
            if label_lower in clabel or clabel in label_lower:
                matched = (cid_str, comm)
                break

        if not matched:
            output.append(f"Community \"{community_label}\" not found.")
            output.append("")
            output.append("Available communities:")
            for cid_str, comm in data.get("communities", {}).items():
                label = comm.get("label", f"Community {cid_str}")
                size = comm.get("size", 0)
                output.append(f"- {label} ({size} items)")
            return "\n".join(output)

        cid_str, comm = matched
        label = comm.get("label", f"Community {cid_str}")
        size = comm.get("size", len(comm.get("members", [])))
        cohesion = comm.get("cohesion", 0)
        members = comm.get("members", [])

        output.append(f"# Community: {label}")
        output.append(f"**{size} items · cohesion: {cohesion}**")
        output.append("")
        output.append("## Members")
        for mid in members[:top_n]:
            m_node = nodes_map.get(mid)
            if m_node:
                m_label = m_node.get("label", mid)
                m_degree = len(edge_index.get(mid, []))
                m_tags = ", ".join(m_node.get("tags_normalized", [])[:3])
                output.append(f"- `{m_label}` ({m_degree} connections, tags: {m_tags})")
        if len(members) > top_n:
            output.append(f"... and {len(members) - top_n} more")

        return "\n".join(output)

    except Exception as e:
        ctx.error(f"Error getting community info: {str(e)}")
        return f"Error getting community info: {str(e)}"